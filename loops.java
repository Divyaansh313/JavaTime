public class Main
{
	public static void main(String[] args) {
		
		int x = 1;
		    
		while (x <= 5){
		    
		    System.out.println(2 * x);
		    
		    x++;
		}
		
		for (int y=1; y<=5; y++){
		    
		    System.out.println(2 * y);
		}
	
		
		for (int y=5; y > 0; y--){
		    
		    System.out.println(2 * y);
		}
	}
}
